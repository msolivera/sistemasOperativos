    public void setearLabels() {

        if (this.info.equals("")) {
            this.grupolbl.setVisible(false);
            this.vacunatoriolbl.setVisible(false);
            this.localidadlbl.setVisible(false);
            this.grupo.setVisible(false);
            this.vacunatorio.setVisible(false);
            this.localidad.setVisible(false);
            this.noHabilitado.setVisible(true);

        } else {
            String[] linea = this.info.split("\\|");
            this.noHabilitado.setVisible(false);
            this.grupolbl.setVisible(true);
            this.vacunatoriolbl.setVisible(true);
            this.localidadlbl.setVisible(true);
            this.grupo.setForeground(Color.GREEN.darker());
            this.vacunatorio.setForeground(Color.GREEN.darker());
            this.grupo.setVisible(true);
            this.vacunatorio.setVisible(true);
            this.localidad.setVisible(true);
            this.grupo.setText(linea[4]);
            this.vacunatorio.setText(linea[2]);
            this.localidad.setText(linea[1]);
        }
    }

    public void reSetearLabels() {

        this.grupo.setText("");
        this.vacunatorio.setText("");
        this.localidad.setText("");
    }

