    private void disponibilidadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_disponibilidadActionPerformed

        if (!this.cedula.getText().isEmpty()) {

            this.manejador.manejarDatos(this.cedula.getText());
            this.info = this.manejador.manejarDatos(this.cedula.getText());
            setearLabels();
        } else {
            this.grupolbl.setVisible(false);
            this.vacunatoriolbl.setVisible(false);
            this.localidadlbl.setVisible(false);
            this.grupo.setVisible(false);
            this.vacunatorio.setVisible(false);
            this.localidad.setVisible(false);
            this.noHabilitado.setVisible(false);
            JOptionPane.showMessageDialog(null, "Debe ingresar su CI");
        }
        if (this.cedula.getText().isEmpty()) {
            this.info = "";
            reSetearLabels();
        }


    }//GEN-LAST:event_disponibilidadActionPerformed

    private void agendarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agendarActionPerformed

        if (this.info.equals("")) {
            JOptionPane.showMessageDialog(null, "Error al intentar agendarse", "Error", JOptionPane.ERROR_MESSAGE);
        } else {

            String[] linea = this.info.split("\\|");
           
                this.mensajeServer = linea[0] + "|" + linea[1] + "|" + linea[2] + "|true|" + linea[4];

           
        }
        try{
            this.cliente.startConnection("192.168.56.1", 80);
            String response = this.cliente.sendMessage(this.mensajeServer);
            JOptionPane.showMessageDialog(null, "Respuesta del Servidor: " + response, "Informaci√≥n", JOptionPane.INFORMATION_MESSAGE);
            this.cliente.stopConnection();
        }catch ( Exception e ) {
//            System.err.println(e);
            JOptionPane.showMessageDialog(null, "Error al conectarse al servidor", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_agendarActionPerformed

